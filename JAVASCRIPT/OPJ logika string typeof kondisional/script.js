// Operator Logika
// &&
x = 12;
console.log(x % 2 == 0 && x < 10);
//
console.log(x % 2 == 0 && x < 14);
// ||
console.log(x % 2 == 0 || x < 10);
// !
console.log(!(x < 20));

// Operator String
var a;
var b;
a = "apple";
b = "pen";
console.log(a + b);
// jika ingin menambahkan spasi
console.log(a + " " + b);
//number and string

console.log(10 + "10");
console.log("10" + 10);
console.log(10 + 10 + "10");
